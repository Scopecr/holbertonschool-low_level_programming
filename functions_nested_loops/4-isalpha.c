#include "main.h"
#include <ctype.h>
#include <stdio.h>
/**
 * _isalpha - Checks if a character is alphabetic.
 * @c: The character to be checked.
 *
 * Return: 1 if character is letter, lowercase or uppercase, 0 otherwise.
 */

int _isalpha(int c)
{
	c = _isalpha(c);
	if (c != 0)
	{
		return (1);
	}
	else
		return (0);
}
